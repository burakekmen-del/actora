  /// Replace variables in copy strings
  static String replaceCopyVariables(
    String copy, {
    required String? friendName,
    required int? dayNumber,
    required int? friendStreak,
    required int? difference,
    required int? friendDay,
    required int? gapSize,
  }) {
    String result = copy;

    if (friendName != null) {
      result = result.replaceAll('{friendName}', friendName);
    }
    if (dayNumber != null) {
      result = result.replaceAll('{dayNumber}', dayNumber.toString());
    }
    if (friendStreak != null) {
      result = result.replaceAll('{friendStreak}', friendStreak.toString());
    }
    if (difference != null) {
      result = result.replaceAll('{difference}', difference.toString());
    }
    if (friendDay != null) {
      result = result.replaceAll('{friendDay}', friendDay.toString());
    }
    if (gapSize != null) {
      result = result.replaceAll('{gapSize}', gapSize.toString());
    }

    return result;
  }

